function normalizeTitles($text)
{
    // 1. تشخیص "فصل دوم" یا "فصل ۲" و استاندارد کردنش
    $patterns = [
        // "فصل یک" ← "فصل ۱"
        '/^[\s]*(فصل)\s*(یک)[\s]*$/mu' => '$1 ۱',
        '/^[\s]*(فصل)\s*(دو)[\s]*$/mu' => '$1 ۲',
        '/^[\s]*(فصل)\s*(سه)[\s]*$/mu' => '$1 ۳',
        '/^[\s]*(فصل)\s*(چهار)[\s]*$/mu' => '$1 ۴',
        '/^[\s]*(فصل)\s*(پنج)[\s]*$/mu' => '$1 ۵',
        '/^[\s]*(فصل)\s*(شش)[\s]*$/mu' => '$1 ۶',
        '/^[\s]*(فصل)\s*(هفت)[\s]*$/mu' => '$1 ۷',
        '/^[\s]*(فصل)\s*(هشت)[\s]*$/mu' => '$1 ۸',
        '/^[\s]*(فصل)\s*(نه)[\s]*$/mu' => '$1 ۹',
        '/^[\s]*(فصل)\s*(ده)[\s]*$/mu' => '$1 ۱۰',
    ];
    
    foreach ($patterns as $pattern => $replacement) {
        $text = preg_replace($pattern, $replacement, $text);
    }
    
    // 2. تبدیل اعداد انگلیسی به فارسی برای اعداد فصل
    // (فعلاً کاری نداریم، چون pdftotext همون فارسی رو می‌ده)
    
    // 3. حذف فاصله‌های اضافی اطراف تیترها
    $text = preg_replace('/^[\s]+(فصل\s+[۰-۹0-9]+)[\s]+$/mu', '$1', $text);
    
    // 4. تشخیص تیترهای فرعی که با "شناخت" یا "انواع" شروع میشن و عدد ندارن
    // (فعلاً نمی‌خوایم کاریشون کنیم، فقط فصل‌ها رو درست می‌کنیم)
    
    return $text;
}
?>